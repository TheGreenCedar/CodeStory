#!/usr/bin/env node

import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { spawnSync } from "node:child_process";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = join(
  repoRoot,
  "plugins",
  "codestory",
  "skills",
  "codestory-grounding",
  "references",
  "generated-cli-syntax.md",
);
const catalogPath = join(repoRoot, "plugins", "codestory", "generated-mcp-catalog.json");
const args = process.argv.slice(2);
const check = args.includes("--check");
const rewriteReferences = args.includes("--rewrite-references");
const cliIndex = args.indexOf("--cli");
const defaultCli = join(repoRoot, "target", "debug", process.platform === "win32" ? "codestory-cli.exe" : "codestory-cli");
const cli = resolve(cliIndex >= 0 ? args[cliIndex + 1] : process.env.CODESTORY_CLI_BIN || defaultCli);

if (!existsSync(cli)) {
  throw new Error(`CodeStory CLI not found at ${cli}. Build it or pass --cli <path>.`);
}

function help(command = []) {
  const result = spawnSync(cli, [...command, "--help"], { encoding: "utf8" });
  if (result.status !== 0) {
    throw new Error(`Failed to read ${[cli, ...command, "--help"].join(" ")}: ${result.stderr}`);
  }
  return result.stdout.replaceAll("\r\n", "\n");
}

function usage(text) {
  const lines = text.split("\n");
  const start = lines.findIndex((line) => line.startsWith("Usage:"));
  if (start < 0) return "";
  const collected = [lines[start].slice("Usage:".length).trim()];
  for (const line of lines.slice(start + 1)) {
    if (!line.startsWith("  ")) break;
    collected.push(line.trim());
  }
  return collected.join(" ").replace(/^codestory-cli(?:\.exe)?\b/u, "codestory-cli");
}

function mcpCatalog() {
  const requests = [
    { jsonrpc: "2.0", id: 1, method: "initialize", params: { protocolVersion: "2024-11-05", capabilities: {}, clientInfo: { name: "catalog-generator", version: "1" } } },
    { jsonrpc: "2.0", id: 2, method: "tools/list" },
    { jsonrpc: "2.0", id: 3, method: "resources/list" },
    { jsonrpc: "2.0", id: 4, method: "resources/templates/list" },
    { jsonrpc: "2.0", id: 5, method: "prompts/list" },
  ];
  const result = spawnSync(cli, ["serve", "--stdio", "--multi-project"], {
    encoding: "utf8",
    input: `${requests.map((request) => JSON.stringify(request)).join("\n")}\n`,
    maxBuffer: 16 * 1024 * 1024,
  });
  if (result.status !== 0) {
    throw new Error(`Failed to read the canonical MCP catalog: ${result.stderr}`);
  }
  const responses = new Map(
    result.stdout
      .split(/\r?\n/u)
      .filter(Boolean)
      .map((line) => JSON.parse(line))
      .filter((response) => response.id !== undefined)
      .map((response) => [response.id, response]),
  );
  for (const id of [2, 3, 4, 5]) {
    if (responses.get(id)?.error || !responses.get(id)?.result) {
      throw new Error(`Canonical MCP catalog request ${id} failed: ${JSON.stringify(responses.get(id))}`);
    }
  }
  return {
    tools: responses.get(2).result.tools,
    resources: responses.get(3).result.resources,
    resourceTemplates: responses.get(4).result.resourceTemplates,
    prompts: responses.get(5).result.prompts,
  };
}

const rootHelp = help();
const commandsBlock = rootHelp.match(/Commands:\n(?<commands>[\s\S]*?)(?:\n\n|\nOptions:)/u)?.groups?.commands ?? "";
const commands = commandsBlock
  .split("\n")
  .map((line) => line.match(/^  (?<name>[a-z][a-z0-9-]*)\s{2,}/u)?.groups?.name)
  .filter((name) => name && name !== "help");

const rows = commands.map((command) => `| \`${command}\` | \`${usage(help([command]))}\` |`);
const generated = [
  "<!-- Generated by scripts/generate-codestory-skill-syntax.mjs. Do not edit. -->",
  "# Generated CodeStory CLI Syntax",
  "",
  "This compact index comes from Clap `--help`, the CLI option source of truth.",
  "Use `<codestory-cli> <command> --help` for the complete current option set.",
  "",
  `Root usage: \`${usage(rootHelp)}\``,
  "",
  "| Command | Usage |",
  "| --- | --- |",
  ...rows,
  "",
].join("\n");
const generatedCatalog = `${JSON.stringify(mcpCatalog(), null, 2)}\n`;

if (check) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, "utf8").replaceAll("\r\n", "\n") : "";
  if (current !== generated) {
    throw new Error("Generated skill syntax is stale. Run node scripts/generate-codestory-skill-syntax.mjs.");
  }
  const currentCatalog = existsSync(catalogPath) ? readFileSync(catalogPath, "utf8").replaceAll("\r\n", "\n") : "";
  if (currentCatalog !== generatedCatalog) {
    throw new Error("Generated MCP catalog is stale. Run node scripts/generate-codestory-skill-syntax.mjs.");
  }
} else {
  writeFileSync(outputPath, generated, "utf8");
  writeFileSync(catalogPath, generatedCatalog, "utf8");
  if (rewriteReferences) {
    const referencesRoot = dirname(outputPath);
    for (const name of readdirSync(referencesRoot).filter((name) => name.endsWith(".md") && name !== "generated-cli-syntax.md")) {
      const path = join(referencesRoot, name);
      let text = readFileSync(path, "utf8").replaceAll("\r\n", "\n");
      text = text.replace(
        /^## Usage\n[\s\S]*?(?=^## )/gmu,
        "## Syntax\n\nSee [generated CLI syntax](generated-cli-syntax.md) for the current command usage.\nUse `<codestory-cli> <command> --help` for the complete option set.\n\n",
      );
      text = text.replace(/^## (?:Arguments|Options|Key Options)\n[\s\S]*?(?=^## )/gmu, "");
      writeFileSync(path, text, "utf8");
    }
  }
}
